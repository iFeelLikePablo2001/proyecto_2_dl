module tb_top;

//////////////////////////////////////////////////////
// SIGNALS
//////////////////////////////////////////////////////

logic clk;
logic reset;

logic [3:0] rows;
logic [3:0] cols;

logic [6:0] seg;
logic [3:0] an;

//////////////////////////////////////////////////////
// DUT
//////////////////////////////////////////////////////

top DUT (

    .clk(clk),
    .reset(reset),

    .rows(rows),
    .cols(cols),

    .seg(seg),
    .an(an)
);

//////////////////////////////////////////////////////
// CLOCK
//////////////////////////////////////////////////////

always #5 clk = ~clk;

//////////////////////////////////////////////////////
// INITIAL
//////////////////////////////////////////////////////

initial begin

    $dumpfile("dump.vcd");
    $dumpvars(0, tb_top);

    clk = 0;
    reset = 1;

    rows = 0;

    #20;
    reset = 0;

end

//////////////////////////////////////////////////////
// SIMULATED KEYPRESSES
//////////////////////////////////////////////////////

initial begin

    //////////////////////////////////////////////////
    // esperar reset
    //////////////////////////////////////////////////

    #100;

    //////////////////////////////////////////////////
    // tecla presionada
    //////////////////////////////////////////////////

    rows = 4'b0001;

    #100;

    rows = 4'b0000;

    //////////////////////////////////////////////////
    // otra tecla
    //////////////////////////////////////////////////

    #200;

    rows = 4'b0010;

    #100;

    rows = 4'b0000;

    //////////////////////////////////////////////////

    #1000;

    $finish;

end

endmodule